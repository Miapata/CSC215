//Add
double AddNumbers(double A, double B) {

	//Sum
	double sum = A + B;

	return sum;
}

//Divide
double DivideNumbers(double a, double b) {

	//Quotient
	double quotient = a / b;

	return quotient;

}


//Multiply
double MultiplyNumbers(double a, double b) {

	//Product
	double product = a * b;

	return product;
}
